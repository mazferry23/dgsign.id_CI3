        $(document).ready(function() {

            $('#detailModal').modal('show');

            $('.content-data').click(function() {

                var title = $(this).data("title");

                var alias = $(this).data("alias");

                var group = $(this).data("group");

                var meja = $(this).data("meja");

                var tamu = $(this).data("tamu");

                var urlimage = $(this).data("urlimage");

                if (urlimage != '') {

                    $("#modalImage").attr("src", urlimage);

                }

                $('#modalTitle').html(title);

                $('#modalAlias').html(alias);

                $('#modalGroup').html(group);

                $('#modaMeja').html(meja);

                $('#modalTamu').html(tamu);

                $('#detailModal').modal('show');

            });

            var timeoutInterval = 3000;

            setInterval(function() {

                $.ajax({

                    type: "POST",

                    url: 'lcd/getguest',

                    success: function(response) {

                        var queue = response.queue;

                        timeoutInterval = (timeoutInterval * queue.length);

                        for (var i = 0; i < queue.length; i++) {

                            setInterval(function() {

                                var title = queue[i].title;

                                var alias = queue[i].alias;

                                var group = queue[i].group;

                                var meja = queue[i].meja;

                                var tamu = queue[i].tamu;

                                var urlimage = queue[i].urlimage;

                                if (urlimage != '') {

                                    $("#modalImage").attr("src", urlimage);

                                }

                                $('#modalTitle').html(title);

                                $('#modalAlias').html(alias);

                                $('#modalGroup').html(group);

                                $('#modaMeja').html(meja);

                                $('#modalTamu').html(tamu);

                                $('#detailModal').modal('show');

                                setInterval(function() {

                                    $('#detailModal').modal('hide');

                                }, 3000);

                            }, (3000 * i))

                        }

                    }

                });

            }, timeoutInterval);

        });
    </script>



